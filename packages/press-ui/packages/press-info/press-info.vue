<template>
  <div
    v-if="info !== null && info !== '' || dot"
    :class="infoClass"
    :style="customStyle"
  >
    {{ dot ? '' : info }}
  </div>
</template>
<script>
import utils from '../wxs-js/utils';

export default {
  props: {
    dot: {
      type: Boolean,
      default: false,
    },
    info: {
      type: [String, null],
      default: null,
    },
    customStyle: {
      type: String,
      default: '',
    },
  },
  computed: {
    infoClass() {
      const { dot, customClass } = this;
      return `van-info ${utils.bem('info', { dot })} ${customClass}`;
    },
  },
};
</script>
